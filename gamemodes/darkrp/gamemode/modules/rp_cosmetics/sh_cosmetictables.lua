function RegisterCosmetic(class, tab)
	local ENT = {}
	ENT.Base = "rp_cosmetics"
	ENT.Type = "anim"
	ENT.PrintName = tab.name
	ENT.WorldModel = tab.model
	ENT.Model = tab.model

	ENT.Category = "Одежда и вещи"
	ENT.Spawnable = true

	if SERVER then
		function ENT:Initialize()
			self:SetModel( self.WorldModel )
			self:PhysicsInit(SOLID_VPHYSICS)
			self:SetMoveType(MOVETYPE_VPHYSICS)
			self:SetSolid(SOLID_VPHYSICS)
			self:SetUseType(SIMPLE_USE)
			local phys = self:GetPhysicsObject()
			phys:Wake()
			self:SetCosmeticType(class)
			self:SetSkin(Cosmetics.Items[class].skin or 0)
		end

		function ENT:Use(a, ply)
			for k, v in pairs(Cosmetics.Items) do
				if k == self:GetCosmeticType() then
					local item = Inventory.GetItem(INV_HATS , self:GetCosmeticType())
					if !item then return true end
					ply:Pickup(self, INV_HATS, self:GetCosmeticType())
					return false
				end
			end
		end
	end

	scripted_ents.Register( ENT, class )
end

for k,v in pairs(Cosmetics.Items) do
	RegisterCosmetic(k, v)
end