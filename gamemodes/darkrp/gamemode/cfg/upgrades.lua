if SERVER then
	hook.Add("PlayerSay", "DonateChangeNickname", function( ply, text )
		if not ply.canChangeNick then return end
		local tbl = string.Split(text, " ")
		if string.lower(tbl[1]) == "/newnick" then
			local name = tbl[2]
			for i = 3, #tbl do
				name = name.." "..tbl[i]
			end

			rp.NotifyAll(NOTIFY_GENERIC, rp.Term('ChangeName'), ply:SteamName(), name)
			rp.data.SetName(ply, name)
			ply:SetNetVar('Name', name)
			ply:SetNVar('Name', name, NETWORK_PROTOCOL_PUBLIC)
			ply.canChangeNick = nil
		end
	end)
end

