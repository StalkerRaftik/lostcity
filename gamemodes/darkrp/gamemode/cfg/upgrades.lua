if (SERVER) then
	local limit = rp.cfg.PropLimit
	hook.Add('PlayerSpawnProp', 'PropUpgrade.PlayerSpawnPropLimit', function(pl)
		local l = limit

  	if pl:IsAdmin() then
      l = l + 5000
    end

		local upgradeCount = pl:GetUpgradeCount('prop_limit_15')
		if (upgradeCount ~= 0) and not pl:IsAdmin() then
			l = l + (15 * upgradeCount)
		end

		if (pl:GetCount('props') >= l) then
			rp.Notify(pl, NOTIFY_ERROR, rp.Term('SboxPropLimit'))
			return false
		end
	end)
end

